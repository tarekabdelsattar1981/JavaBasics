package com.syntax.class07;

public class WhileDoWHILE {
public static void main(String[]args) {
	int num=11;
	
	while(num<5) {
		System.out.println("Hello");
		num++;
	}
	System.out.println("------------using DO wjile Loop-------");
	// hello 5 times
	
	int num1=6;
	
	do {
		System.out.println("Hello");
		num1++;
	}while(num1<5);
	
	
	
	
	
	
}
}
