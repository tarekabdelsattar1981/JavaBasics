package com.syntax.class09;

public class Recap {
	public static void main(String[]args) {
		
		for(int i=1; i<=3; i++) {
			for(int j=1; j<=3; j++) {
				System.out.println(i+" "+j);
			}
		
			
		}
		// using nested loop from 10 to 99
		
		for(int i=1; i<=9; i++) {
			for(int x=0; x<=9; x++) {
				System.out.println(i+""+x);
			}
		}
	}

}
