package Practice;

public class P4 {
	public static void main(String[] args) {
		
		for(int x=7; x>=1; x--) {
			
			for(int y=1; y<=x; y++) {
				
				System.out.print(y+" ");
			}
			System.out.println();
		}
		for(int a=2; a<=7; a++) {
			
			for(int b=1; b<=a; b++) {
				
				System.out.print(b+" ");
			}
			System.out.println();
		}
		
		
		

}
}